// Program to use variables to store values and do the mathematical operations
public class vari
{
    public static void main(String[] args)
    {
        int a = 9;
        int b = 6;
        int sum = a + b;
        int diff = a - b;
        int mul = a * b;
        int div = a / b;
        int rem = a % b;

        System.out.println("The sum is "+ sum); // Addition
        System.out.println("The difference is "+diff); // Subtraction
        System.out.println("The product is "+mul); // Product
        System.out.println("The quotient is "+div); // Division
        System.out.println("The remainder is "+rem); // Remainder
    }
}
